                                                            <h3><strong>Kerja Praktek</strong></h3>
                                                            <p>Kerja praktek dilakukan oleh mahasiswa untuk memberikan
                                                                gambaran tentang dunia kerja yang sesungguhnya. Kerja
                                                                praktek dapat dilakukan di industri maupun lembaga riset
                                                                yang terkait dengan bidang ilmu di Jurusan Sistem
                                                                Komputer.<br />Berikut adalah <strong><a
                                                                        href="https://tekkom.ft.undip.ac.id/wp-content/uploads/2022/07/009-20220721-Pengambilan-Pelaksanaan-KP.pdf"
                                                                        target="_blank" rel="noopener">SOP Pengambilan dan
                                                                        Pelaksanaan Mata Kuliah Kerja Praktik
                                                                        2022</a></strong></p>
                                                            <div>
                                                                <p><strong>Syarat-Syarat Kerja Praktek</strong></p>
                                                            </div>
                                                            <ol>
                                                                <li>Perolehan <strong>SKS &gt;= 70</strong> SKS tanpa nilai
                                                                    E. Dan <strong>IPK &gt;= 2,00</strong></li>
                                                                <li>Dilaksanakan dalam waktu <strong>minimal 1
                                                                        bulan</strong> di sebuah perusahaan/ instasi/lembaga
                                                                    (tempat KP).</li>
                                                                <li>KP harus <strong>diseminarkan</strong>.</li>
                                                            </ol>
                                                            <p><strong>Prosedur Kerja Praktek</strong></p>
                                                            <ol>
                                                                <li>Mahasiswa mencari tempat/lokasi Kerja Praktek sekaligus
                                                                    mencari Dosen Pembimbing Kerja Praktek (untuk permohonan
                                                                    dosen pembimbing bisa menghubungi Koordinator KP).</li>
                                                                <li>Setelah mendapatkan tempat/lokasi Kerja Praktek,
                                                                    Mahasiswa mengajukan Surat Permohonan Kerja Praktek (<a
                                                                        href="http://akademik.ft.undip.ac.id/index.php">http://akademik.ft.undip.ac.id/index.php</a>)
                                                                    dan proposal pengajuan pelaksanaan Kerja Praktek ke
                                                                    Dosen Pembimbing untuk dibawa ke perusahaan (Proposal
                                                                    dan Surat dibuat jika dibutuhkan perusahaan).</li>
                                                                <li>Jika mahasiswa sudah diterima Kerja Praktek, harap
                                                                    menyimpan bukti surat/email pernyataan diterima Kerja
                                                                    Praktek oleh perusahaan. Form yang perlu disiapkan
                                                                    adalah Form Permohonan Kerja Praktek (KP A1). Untuk Form
                                                                    KP A1, setelah ditanda-tangani bisa diserahkan ke
                                                                    Koordinator Kerja Praktek bersama dengan surat/email
                                                                    pernyataan diterima Kerja Praktek oleh perusahaan
                                                                    terkait.</li>
                                                                <li>Jika mahasiswa melaksanakan Kerja Praktek bersamaan
                                                                    dengan jadwal kuliah, maka Mahasiswa harus mengumpulkan
                                                                    Form List Absensi Kuliah (KP-B5) ke Bagian Akademik
                                                                    Departemen sebelum perkuliahan berlangsung.</li>
                                                                <li>Selama proses Kerja Praktek, form yang harus dibawa
                                                                    adalah Form Presensi dan Nilai Lapangan (KP A2) yang
                                                                    berisikan jadwal absensi selama Kerja Praktek dan Nilai
                                                                    Kerja Praktek yang diberikan oleh dosen pembimbing
                                                                    lapangan.</li>
                                                                <li>Setelah Kerja Praktek selesai, Mahasiswa segera menyusun
                                                                    laporan dan makalah Kerja Praktek dan dikonsultasikan
                                                                    dengan Dosen Pembimbing Kerja Praktek hingga mendapatkan
                                                                    tanda tangan persetujuan dari Dosen Pembimbing Kerja
                                                                    Praktek.</li>
                                                                <li>Syarat seminar Kerja Praktek adalah sudah mendapatkan
                                                                    pengesahan laporan dan makalah dari Dosen Pembimbing
                                                                    Kerja Praktek serta sudah menghadiri seminar Kerja
                                                                    Praktek sebanyak 10 kali (dibuktikan dengan kartu
                                                                    kehadiran Kerja Praktek).</li>
                                                                <li>Saat seminar Kerja Praktek, form yang disiapkan selama
                                                                    seminar adalah Form Presensi Seminar (KP B2) dan Form
                                                                    Nilai Seminar (KP B3).</li>
                                                                <li>Setelah selesai seminar, Mahasiswa menyelesaikan revisi
                                                                    laporan jika ada, jika tidak ada maka Mahasiswa bisa
                                                                    melanjutkan ke proses nilai KP.</li>
                                                                <li>Untuk mendapatkan nilai Kerja Praktek harus melengkapi
                                                                    kelengkapan-kelengkapan yang meliputi:
                                                                    <ul>
                                                                        <li>Laporan Kerja Praktek lengkap (lembar
                                                                            pengesahan, makalah, cv, dan lampiran-lampiran
                                                                            lain)</li>
                                                                        <li>Kartu hadir seminar (sudah diisi minimal 10 kali
                                                                            hadir seminar)</li>
                                                                        <li>Absensi pelaksanaan seminar Kerja Praktek (KP
                                                                            B2)</li>
                                                                        <li>Nilai seminar dan laporan dari pembimbing (KP
                                                                            B3)</li>
                                                                        <li>Absensi dan nilai Kerja Praktek dari pembimbing
                                                                            di lapangan (KP A2)</li>
                                                                        <li>Form permohonan nilai Kerja Praktek yang sudah
                                                                            diketik atau dituliskan nama dan nim mahasiswa
                                                                            yang bersangkutan.</li>
                                                                    </ul>
                                                                </li>
                                                                <li>Semua kelengkapan Kerja Praktek dijadikan satu dan
                                                                    diserahkan ke Koordinator Kerja Praktek.</li>
                                                            </ol>
                                                            <p><strong>Administrasi Kerja Praktek</strong><br />Mahasiswa
                                                                yang menjalankan kerja praktek harus memperhatikan beberapa
                                                                hal berikut ini:<br />1. Masa pelaksanaan kerja Praktek
                                                                <strong>sebaiknya</strong> dilakukan saat <strong>libur
                                                                    kuliah</strong>.<br />2. Menyerahkan surat
                                                                <strong>pernyataan diterima Kerja Praktek</strong> yang
                                                                memuat <strong>Nama</strong>, <strong>NIM</strong>, dan
                                                                <strong>Tanggal Pelaksanaan</strong> Kerja Praktek.
                                                            </p>
                                                            <ul>
                                                                <li><strong>NOTE</strong> : Mahasiswa yang melaksanakan
                                                                    Kerja Praktek bersamaan dengan jalannya kuliah harus
                                                                    menyerahkan surat pernyataan diterima kerja praktek
                                                                    sebanyak mata kuliah yang diikuti <strong>paling lambat
                                                                        pada akhir minggu pertama pekuliahan
                                                                        berlangsung</strong>. Bagi mahasiswa yang terlambat
                                                                    menyerahkan surat pernyataan diterima KP akan dianggap
                                                                    tidak izin kuliah.</li>
                                                            </ul>
                                                            <p>3. Surat pernyataan tersebut harus diserahkan ke bagian
                                                                <strong>administrasi</strong> di Jurusan Teknik
                                                                Komputer.<br />4. Jika ada mahasiswa yang melaksanakan Kerja
                                                                Praktek saat masa perkuliahan maka harus menyerahkan
                                                                <strong>surat pernyataan diterima Kerja Praktek</strong> dan
                                                                <strong>Form KP-B5</strong> <strong>sebelum perkuliahan
                                                                    dimulai</strong>.
                                                            </p>
                                                            <p><strong>Syarat Seminar Kerja Praktek</strong></p>
                                                            <ol>
                                                                <li>Laporan KP disetujui dan disahkan oleh pembimbing
                                                                    lapangan, pembimbing dari Sistem Komputer dan Ketua
                                                                    Jurusan, diserahkan ke koordinator KP.</li>
                                                                <li>Jika dalam jangka waktu maksimal 3 bulan terhitung sejak
                                                                    berakhirnya pelaksanaan KP, mahasiswa yang bersangkutan
                                                                    tidak membuat laporan dan melaksanakan seminar KP, maka
                                                                    diberikan perpanjangan 2 bulan lagi dengan tugas
                                                                    tambahan dari pembimbing KP – Sistem Komputer (Form
                                                                    KP-B4) dan menandatangani perjanjian, bila sampai 2
                                                                    bulan lagi tidak selesai, maka KP tersebut dibatalkan
                                                                    dan mahasiswa harus mengulang KP.</li>
                                                                <li>Jika laporan sudah selesai, mahasiswa membuat makalah
                                                                    hasil KP dengan standar jurnal IEEE dan sudah disetujui
                                                                    oleh dosen pembimbing.</li>
                                                                <li>Untuk seminar KP, mahasiswa disyaratkan telah mengikuti
                                                                    seminar Kerja Praktek mahasiswa lain minimal 10 kali,
                                                                    dengan dibuktikan pada kartu hijau, kartu hadir seminar
                                                                    KP.</li>
                                                            </ol>
                                                            <p><strong>Prosedur Seminar Kerja Praktek</strong></p>
                                                            <ol>
                                                                <li>Mengisi Form Seminar Kerja Praktek (KP-B1) dan disetujui
                                                                    Pembimbing.</li>
                                                                <li>Melampirkan bukti ikut seminar KP mahasiswa lain minimal
                                                                    10 kali (kartu hijau)</li>
                                                                <li>Membuat surat undangan Seminar Kerja Praktek yang
                                                                    disetujui oleh pembimbing, Koordinator Kerja Praktek dan
                                                                    Ketua Jurusan</li>
                                                                <li>Menyiapkan Daftar Hadir Seminar dan Form Nilai Kerja
                                                                    Praktek dan Seminar (Form KP-B2)</li>
                                                                <li>Seminar dihadiri minimal 10 mahasiswa Sistem Komputer.
                                                                </li>
                                                            </ol>
                                                            <p><strong>Laporan Kerja Praktek</strong></p>
                                                            <ol>
                                                                <li>Format Penulisan Laporan terbaru lihat di buku petunjuk
                                                                    yang baru (mengikuti format penulisan Tugas Akhir).</li>
                                                                <li>Laporan KP dibuat sebanyak 4 eksemplar dengan perincian
                                                                    sebagai berikut;
                                                                    <ul>
                                                                        <li>1 untuk tempat KP.</li>
                                                                        <li>1 untuk perpustakaan Jurusan Sistem Komputer
                                                                        </li>
                                                                        <li>1 untuk dosen pembimbing (sesuai permintaan
                                                                            dosen pembimbing)</li>
                                                                        <li>1 untuk arsip mahasiswa yang bersangkutan.</li>
                                                                    </ul>
                                                                </li>
                                                                <li>Laporan disahkan oleh Pembimbing dan Ketua Jurusan.</li>
                                                                <li>Penyerahan Laporan KP kepada koordinator KP disertai :
                                                                    <ul>
                                                                        <li>Nilai Kerja Praktek dan Seminar (Form KP-B3)
                                                                            dari Dosen Pembimbing.</li>
                                                                        <li>Absensi Kerja Praktek dan Nilai dari pembimbing
                                                                            lapangan (Form KP-A2)</li>
                                                                        <li>CD laporan KP, berlabel nim, nama dan judul KP,
                                                                            yang berisi :
                                                                            <ul>
                                                                                <li>Dalam folder ‘laporan_kp’ berisi file
                                                                                    laporan lengkap dalam format word.</li>
                                                                                <li>Dalam ‘root dir’ (tidak masuk folder)
                                                                                    berisi file makalah seminar KP, format
                                                                                    word dan pdf, dengan nama file seperti
                                                                                    contoh; NIM_MKP.doc dan NIM_MKP.pdf</li>
                                                                                <li>Dalam ‘root dir” berisi file CV dalam
                                                                                    format word, contoh : NIM_CV.doc</li>
                                                                                <li>Dalam folder ‘root dir’ berisi file foto
                                                                                    diri dalam format JPG : contoh NIM.jpg
                                                                                </li>
                                                                            </ul>
                                                                        </li>
                                                                    </ul>
                                                                </li>
                                                            </ol>
                                                            <p>Hasil Kerja Praktek Kerja Praktek yang menghasilkan suatu
                                                                bentuk perangkat keras atau perangkat lunak akan menjadi
                                                                milik Jurusan/Program Studi Sistem Komputer Undip dan pihak
                                                                jurusan berhak mempergunakan atau mempublikasikan hasil KP
                                                                tersebut terutama yang bersifat penelitian (misalnya di
                                                                laboratorium sistem komputer).</p>
                                                        </div>
